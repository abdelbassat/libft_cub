#include "libft.h"

t_list	*ft_lstnew(void *content)
{
	t_list	*new_node;

	new_node = (t_list *)malloc(sizeof(t_list));
	if (!new_node)
		return (0);
	new_node->content = ft_strdup(content);
	// new_node->color = 0;
	new_node->next = NULL;
	return (new_node);
}
